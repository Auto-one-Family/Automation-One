  /**
   * Auto-generate a zone dashboard based on devices in the zone.
   * Uses SENSOR_TYPE_CONFIG for category-aware widget selection and Y-axis defaults.
   * Layout: time-series (line-chart) at top full-width, gauges middle half-width, actuators bottom.
   * After creation, asynchronously enriches sensor widgets with alert-config thresholds (P8-A3).
   */
  function generateZoneDashboard(zoneId: string, devices: { esp_id?: string; sensors?: { gpio: number; sensor_type: string; name?: string | null; config_id?: string }[]; actuators?: { gpio: number; actuator_type?: string; name?: string | null }[] }[], zoneName: string): DashboardLayout | null {
    const existing = layouts.value.find(l => l.autoGenerated && l.scope === 'zone' && l.zoneId === zoneId)

    // Collect all sensors and actuators
    type SensorEntry = { espId: string; gpio: number; sensorType: string; name: string | null; category: SensorCategoryId; configId?: string }
    type ActuatorEntry = { espId: string; gpio: number; actuatorType: string; name: string | null }

    const timeSeriesSensors: SensorEntry[] = []
    const gaugeSensors: SensorEntry[] = []
    const otherSensors: SensorEntry[] = []
    const actuators: ActuatorEntry[] = []

    for (const device of devices) {
      const espId = device.esp_id || ''
      for (const s of (device.sensors || [])) {
        const config = SENSOR_TYPE_CONFIG[s.sensor_type]
        const category: SensorCategoryId = config?.category || 'other'
        const mapping = CATEGORY_WIDGET_MAP[category]

        const entry: SensorEntry = {
          espId,
          gpio: s.gpio,
          sensorType: s.sensor_type,
          name: s.name || null,
          category,
          configId: s.config_id,
        }

        if (mapping.type === 'line-chart') timeSeriesSensors.push(entry)
        else if (mapping.type === 'gauge') gaugeSensors.push(entry)
        else otherSensors.push(entry)
      }

      for (const a of (device.actuators || [])) {
        actuators.push({
          espId,
          gpio: a.gpio,
          actuatorType: a.actuator_type || 'relay',
          name: a.name || null,
        })
      }
    }

    const widgets: DashboardWidget[] = []
    let currentY = 0

    // Row 1: Time-series sensors (full width, h:3 each)
    for (const s of timeSeriesSensors) {
      const config = SENSOR_TYPE_CONFIG[s.sensorType]
      const mapping = CATEGORY_WIDGET_MAP[s.category]
      widgets.push({
        id: `auto-${zoneId}-s-${s.gpio}-${Date.now()}-${widgets.length}`,
        type: 'line-chart',
        x: 0, y: currentY, w: 12, h: 3,
        config: {
          espId: s.espId,
          gpio: s.gpio,
          sensorId: s.espId ? `${s.espId}:${s.gpio}:${s.sensorType}` : undefined,
          sensorType: s.sensorType,
          zoneId,
          title: s.name || config?.label || s.sensorType,
          timeRange: mapping.timeRange,
          showThresholds: false,
          ...(config ? { yMin: config.min, yMax: config.max } : {}),
        },
      })
      currentY += 3
    }

    // Row 2+: Gauges (half width w:6, h:2, 2 per row)
    let gaugeCol = 0
    for (const s of gaugeSensors) {
      const config = SENSOR_TYPE_CONFIG[s.sensorType]
      widgets.push({
        id: `auto-${zoneId}-s-${s.gpio}-${Date.now()}-${widgets.length}`,
        type: 'gauge',
        x: gaugeCol * 6, y: currentY, w: 6, h: 2,
        config: {
          espId: s.espId,
          gpio: s.gpio,
          sensorId: s.espId ? `${s.espId}:${s.gpio}:${s.sensorType}` : undefined,
          sensorType: s.sensorType,
          zoneId,
          title: s.name || config?.label || s.sensorType,
          showThresholds: false,
          ...(config ? { yMin: config.min, yMax: config.max } : {}),
        },
      })
      gaugeCol++
      if (gaugeCol >= 2) { gaugeCol = 0; currentY += 2 }
    }
    if (gaugeCol > 0) currentY += 2 // close partial row

    // Row 3+: Other sensors (sensor-card, half width)
    let otherCol = 0
    for (const s of otherSensors) {
      const config = SENSOR_TYPE_CONFIG[s.sensorType]
      widgets.push({
        id: `auto-${zoneId}-s-${s.gpio}-${Date.now()}-${widgets.length}`,
        type: 'sensor-card',
        x: otherCol * 6, y: currentY, w: 6, h: 2,
        config: {
          espId: s.espId,
          gpio: s.gpio,
          sensorId: s.espId ? `${s.espId}:${s.gpio}:${s.sensorType}` : undefined,
          sensorType: s.sensorType,
          zoneId,
          title: s.name || config?.label || s.sensorType,
        },
      })
      otherCol++
      if (otherCol >= 2) { otherCol = 0; currentY += 2 }
    }
    if (otherCol > 0) currentY += 2

    // Bottom: Actuators (third width w:4, h:2, 3 per row)
    let actCol = 0
    for (const a of actuators) {
      widgets.push({
        id: `auto-${zoneId}-a-${a.gpio}-${Date.now()}-${widgets.length}`,
        type: 'actuator-card',
        x: actCol * 4, y: currentY, w: 4, h: 2,
        config: {
          espId: a.espId,
          gpio: a.gpio,
          actuatorId: a.espId ? `${a.espId}:${a.gpio}` : undefined,
          zoneId,
          title: a.name || a.actuatorType,
        },
      })
      actCol++
      if (actCol >= 3) { actCol = 0; currentY += 2 }
    }

    if (widgets.length === 0) return null

    // Collect configId→widget index mapping for async threshold enrichment (P8-A3)
    const configIdToWidgetIndices = new Map<string, number[]>()
    const allSensors = [...timeSeriesSensors, ...gaugeSensors, ...otherSensors]
    for (let i = 0; i < allSensors.length; i++) {
      const s = allSensors[i]
      if (s.configId && i < widgets.length) {
        const indices = configIdToWidgetIndices.get(s.configId) || []
        indices.push(i)
        configIdToWidgetIndices.set(s.configId, indices)
      }
    }

    let resultLayout: DashboardLayout

    if (existing) {
      const ok = applyLayoutPatch(
        existing.id,
        { widgets },
        { reasonMessage: 'Auto-Dashboard für Zone aktualisiert' },
      )
      if (!ok) {
        return null
      }
      resultLayout = layouts.value.find((l) => l.id === existing.id)!
    } else {
      resultLayout = {
        id: `dash-auto-${zoneId}-${Date.now()}`,
        name: `${zoneName} Dashboard`,
        description: `Automatisch generiert für Zone ${zoneName}`,
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
        widgets,
        scope: 'zone',
        zoneId,
        autoGenerated: true,
        target: { view: 'monitor', placement: 'inline' },
        syncFlags: buildSyncFlags(undefined, null, {
          status: 'local_only',
          dirty: true,
          server_synced: false,
          last_sync_message: 'Auto-Dashboard erstellt',
        }),
      }
      layouts.value.push(resultLayout)
      persistLayouts()
      syncLayoutToServer(resultLayout.id)
    }

    // Fire-and-forget: enrich widgets with alert-config thresholds (P8-A3)
    if (configIdToWidgetIndices.size > 0) {
      enrichWidgetsWithAlertThresholds(resultLayout.id, widgets, configIdToWidgetIndices)
    }

    return resultLayout
  }

  /**
   * Async enrichment: fetch alert-config thresholds for sensor widgets and apply as defaults.
   * Runs fire-and-forget after generateZoneDashboard creates the layout synchronously.
   */
  async function enrichWidgetsWithAlertThresholds(
    layoutId: string,
    widgets: DashboardWidget[],
    configIdToWidgetIndices: Map<string, number[]>,
  ): Promise<void> {
    const entries = [...configIdToWidgetIndices.entries()]
    const results = await Promise.allSettled(
      entries.map(([configId]) => sensorsApi.getAlertConfig(configId))
    )

    let changed = false
    for (let i = 0; i < results.length; i++) {
      const result = results[i]
      if (result.status !== 'fulfilled') continue

      const alertConfig = result.value.alert_config as Record<string, any> | undefined
      const thresholds = alertConfig?.custom_thresholds as
        | { warning_min?: number | null; warning_max?: number | null; critical_min?: number | null; critical_max?: number | null }
        | undefined
      if (!thresholds) continue

      const hasAny = thresholds.warning_min != null || thresholds.warning_max != null
        || thresholds.critical_min != null || thresholds.critical_max != null
      if (!hasAny) continue

      const widgetIndices = entries[i][1]
      for (const widgetIdx of widgetIndices) {
        const widget = widgets[widgetIdx]
        if (!widget) continue
        widget.config = {
          ...widget.config,
          showThresholds: true,
          ...(thresholds.warning_min != null ? { warnLow: thresholds.warning_min } : {}),
          ...(thresholds.warning_max != null ? { warnHigh: thresholds.warning_max } : {}),
          ...(thresholds.critical_min != null ? { alarmLow: thresholds.critical_min } : {}),
          ...(thresholds.critical_max != null ? { alarmHigh: thresholds.critical_max } : {}),
        }
        changed = true
      }
    }

    if (changed) {
      applyLayoutPatch(
        layoutId,
        { widgets },
        { reasonMessage: 'Alert-Schwellenwerte in Widgets angereichert' },
      )
    }
  }

